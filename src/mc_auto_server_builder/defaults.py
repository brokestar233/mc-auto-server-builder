SUPPORTED_JAVA_VERSIONS = [8, 11, 17, 21, 25]

JAVA_VERSION_TO_JVM_PARAMS = {
    8: [*COMMON_JVM_PARAMS, *JVM_PARAMS_DW8],  # Java 8 使用龙井（Dragonwell）参数
    11: [*COMMON_JVM_PARAMS, *JVM_PARAMS_DW11],  # Java 11 使用龙井（Dragonwell）参数
    17: [*COMMON_JVM_PARAMS, *JVM_PARAMS_GRAALVM17],  # Java 17 使用 GraalVM 参数
    21: [*COMMON_JVM_PARAMS, *JVM_PARAMS_GRAALVM17],  # Java 21 按要求复用 GraalVM 17 参数
    25: [*COMMON_JVM_PARAMS, *JVM_PARAMS_GRAALVM25],  # Java 25 使用 GraalVM 参数
}


def get_jvm_params_for_java_version(version: int) -> list[str]:
    """按 Java 主版本返回预置 JVM 参数，不支持时默认回退到 Java 21 档位。"""
    return list(JAVA_VERSION_TO_JVM_PARAMS.get(version, JAVA_VERSION_TO_JVM_PARAMS[21]))


def get_common_jvm_params() -> list[str]:
    """返回仅通用 JVM 参数（用于 GraalVM 下载失败回退到其他发行版时）。"""
    return list(COMMON_JVM_PARAMS)
